package net.janbuchinger.code.mishmash;

/**
 * GC is an abbreviation for &quot;german chars&quot; and may be pretty unusable.
 * <p>
 * Maybe it will be used as cheat sheet for unicode codes of special chars.
 * 
 * @author Jan Buchinger
 * 
 */
public final class GC {

	/**
	 * &quot;Umlaut A&quot; as unicode character is &quot;\u00C4&quot;.
	 * 
	 * @return The unicode character <code>String</code> for &quot;Umlaut A&quot;.
	 */
	public final static String Ae() {
		return "\u00C4";
	}

	/**
	 * &quot;Umlaut a&quot; as unicode character is &quot;\u00E4&quot;.
	 * 
	 * @return The unicode character <code>String</code> for &quot;Umlaut a&quot;.
	 */
	public final static String ae() {
		return "\u00E4";
	}

	/**
	 * &quot;Umlaut U&quot; as unicode character is &quot;\u00DC&quot;.
	 * 
	 * @return The unicode character <code>String</code> for &quot;Umlaut U&quot;.
	 */
	public final static String Ue() {
		return "\u00DC";
	}

	/**
	 * &quot;Umlaut u&quot; as unicode character is &quot;\u00FC&quot;.
	 * 
	 * @return The unicode character <code>String</code> for &quot;Umlaut u&quot;.
	 */
	public final static String ue() {
		return "\u00FC";
	}

	/**
	 * &quot;Umlaut O&quot; as unicode character is &quot;\u00D6&quot;.
	 * 
	 * @return The unicode character <code>String</code> for &quot;Umlaut O&quot;.
	 */
	public final static String Oe() {
		return "\u00D6";
	}

	/**
	 * &quot;Umlaut o&quot; as unicode character is &quot;\u00F6&quot;.
	 * 
	 * @return The unicode character <code>String</code> for &quot;Umlaut o&quot;.
	 */
	public final static String oe() {
		return "\u00F6";
	}
}
