package net.janbuchinger.code.mishmash;

/**
 * GC is an abbreviation for "german chars" and may be pretty unusable.
 * <p>
 * Maybe it will be used as cheat sheet for unicode codes of special chars.
 * 
 * @author Jan Buchinger
 * 
 */
public final class GC {

	/**
	 * "Umlaut A" as unicode character is "\u00C4".
	 * 
	 * @return The unicode character <code>String</code> for "Umlaut A".
	 */
	public final static String Ae() {
		return "\u00C4";
	}

	/**
	 * "Umlaut a" as unicode character is "\u00E4".
	 * 
	 * @return The unicode character <code>String</code> for "Umlaut a".
	 */
	public final static String ae() {
		return "\u00E4";
	}

	/**
	 * "Umlaut U" as unicode character is "\u00DC".
	 * 
	 * @return The unicode character <code>String</code> for "Umlaut U".
	 */
	public final static String Ue() {
		return "\u00DC";
	}

	/**
	 * "Umlaut u" as unicode character is "\u00FC".
	 * 
	 * @return The unicode character <code>String</code> for "Umlaut u".
	 */
	public final static String ue() {
		return "\u00FC";
	}

	/**
	 * "Umlaut O" as unicode character is "\u00D6".
	 * 
	 * @return The unicode character <code>String</code> for "Umlaut O".
	 */
	public final static String Oe() {
		return "\u00D6";
	}

	/**
	 * "Umlaut o" as unicode character is "\u00F6".
	 * 
	 * @return The unicode character <code>String</code> for "Umlaut o".
	 */
	public final static String oe() {
		return "\u00F6";
	}
}
